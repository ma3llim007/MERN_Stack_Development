// Loops can execute a block of code as long as a specified condition is true.
// The while loop loops through a block of code as long as a specified condition is true.


// Example
const cars = ["BMW", "Volvo", "Mini"];
function Example() {
    let i = 0;
    while (cars[i]) {
        console.log(cars[i]);
        i++;
    }
}
Example();